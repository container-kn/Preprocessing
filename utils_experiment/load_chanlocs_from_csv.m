function chanlocs = load_chanlocs_from_csv(csvFile)
% Build EEGLAB-style chanlocs from CSV

T = readtable(csvFile);

nCh = height(T);
chanlocs = struct( ...
    'labels',     cell(nCh,1), ...
    'X',          num2cell(zeros(nCh,1)), ...
    'Y',          num2cell(zeros(nCh,1)), ...
    'Z',          num2cell(zeros(nCh,1)), ...
    'theta',      num2cell(zeros(nCh,1)), ...
    'radius',     num2cell(zeros(nCh,1)), ...
    'sph_theta',  num2cell(zeros(nCh,1)), ...
    'sph_phi',    num2cell(zeros(nCh,1)), ...
    'sph_radius', num2cell(zeros(nCh,1)) ...
    );

for i = 1:nCh
    chanlocs(i).labels     = T.labels{i};
    chanlocs(i).X          = T.X(i);
    chanlocs(i).Y          = T.Y(i);
    chanlocs(i).Z          = T.Z(i);
    chanlocs(i).theta      = T.theta(i);
    chanlocs(i).radius     = T.radius(i);
    chanlocs(i).sph_theta  = T.sph_theta(i);
    chanlocs(i).sph_phi    = T.sph_phi(i);
    chanlocs(i).sph_radius = T.sph_radius(i);
end
end
